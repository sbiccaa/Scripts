//Word Blanks 
function wordBlanks(myNoun, myAdjective, myVerb, myAdverb ){

var result = "";

result += "The " + myAdjective + " " + myNoun + " " + myVerb + "" + " To the park " + myAdverb

return result;

}

console.log(wordBlanks("dog", "big", "ran", "quickly"));
console.log(wordBlanks("ban", "public", "in", "fraud"));
 
